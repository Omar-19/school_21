int		ft_iterative_factorial(int nb)
{
	int res;

	res = 1;
	if (nb == 0)
		return (1);
	if (nb > 0 && nb < 13)
	{
		while (nb > 0)
		{
			res = res * nb;
			nb--;
		}
		return (res);
	}
	return (0);
}
